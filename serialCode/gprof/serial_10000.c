//create tuple for the minimum so we know the value to compare to and the index to swap with
struct min_tuple {
	int min_value; //need for the reduction clause, can't access arr[index]
	int min_index;
};

void swap(int *xp, int *yp)
{
	int temp = *xp;
	*xp = *yp;
	*yp = temp;
}

